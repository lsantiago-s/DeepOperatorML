C
	SUBROUTINE DETERMINEkn1(Kn1, ZETA)
C
	USE Nucleos
c	USE Numerical_Libraries
C
	IMPLICIT NONE
C
	COMPLEX*16, DIMENSION(3,3), INTENT(OUT) :: Kn1
	REAL*8, INTENT(IN) :: ZETA
C
	REAL*8, DIMENSION(100,8) :: PROPERTIES
	REAL*8 :: R, OMEGA
	INTEGER :: N, Nload, Nrec
C
	REAL*8 :: ETA, RHO, ZN, ZN1, J0, J1, J2
	COMPLEX*16 :: C11, C12, C13, C33, C44
	COMPLEX*16 :: ALPHA, BETAb, GAMMAb, SIGMA, KAPPA, DELTA
	COMPLEX*16 :: CURL1, CURL2, KSI1, KSI2, KSI3, PHI
C	COMPLEX*16 :: A1, A2, A3, A4, A5, A6, A7, A8
C	COMPLEX*16 :: B51, B52, B53, B41, B42, B43, B21, B22
	COMPLEX*16 :: G1, G2, G3, G4, G5
	COMPLEX*16 :: H41, H42, H43, H21, H22
	COMPLEX*16, DIMENSION(3,3) :: Gn, Fn
	COMPLEX*16 :: LAMBDA
C
	COMMON /PROPS/ PROPERTIES
	COMMON /PHYSICS/ R, OMEGA
	COMMON /SIZE/ N, Nload, Nrec
C
C	PROPERTIES = [C11 C12 C13 C33 C44 eta RHO Zn Z(n+1)]
	ETA = PROPERTIES(N+1,6)
	C11 = PROPERTIES(N+1,1)*DCMPLX(1.0D0, ETA)
	C12 = PROPERTIES(N+1,2)*DCMPLX(1.0D0, ETA)
	C13 = PROPERTIES(N+1,3)*DCMPLX(1.0D0, ETA)
	C33 = PROPERTIES(N+1,4)*DCMPLX(1.0D0, ETA)
	C44 = PROPERTIES(N+1,5)*DCMPLX(1.0D0, ETA)
	RHO = PROPERTIES(N+1,7)
	zN  = PROPERTIES(N+1,8)
C	zN1 = PROPERTIES(N+1,9) ! zN1 is not needed in this case.
C
	ALPHA = C33/C44*DCMPLX(1.0D0, ETA)
	BETAb = C11/C44*DCMPLX(1.0D0, ETA)
	KAPPA = (C13 + C44)/C44*DCMPLX(1.0D0, ETA)
	GAMMAb = (1.0D0 + ALPHA*BETAb - KAPPA**2.0D0)*DCMPLX(1.0D0, ETA)
	SIGMA = 0.5D0*(C11-C12)/C44*DCMPLX(1.0D0, ETA)
C
	DELTA = CDSQRT(RHO*OMEGA**2.0D0/C44)*DCMPLX(1.0D0, ETA)
C        
	PHI = (GAMMAb*ZETA**2.0D0-1.0D0-ALPHA)**2.0D0
     *-4.0D0*ALPHA*(BETAb*ZETA**4.0D0-(BETAb+1.0D0)*ZETA**2.0D0+1.0D0)
C
	KSI1 = CDSQRT((GAMMAb*ZETA**2.0D0-1.0D0-ALPHA+CDSQRT(PHI))
     *			/(2.0D0*ALPHA))
	KSI2 = CDSQRT((GAMMAb*ZETA**2.0D0-1.0D0-ALPHA-CDSQRT(PHI))
     *			/(2.0D0*ALPHA))
	KSI3 = CDSQRT(SIGMA*ZETA**2.0D0-1.0D0)
C
	CURL1 = (ALPHA*KSI1**2.0D0-ZETA**2.0D0+1.0D0)/(KAPPA*ZETA**2.0D0)
	CURL2 = (ALPHA*KSI2**2.0D0-ZETA**2.0D0+1.0D0)/(KAPPA*ZETA**2.0D0)
C
c	J0 = Jm(0,DREAL(DELTA)*ZETA,R) ! Bessel function of 1st kind and order 0
c	J1 = Jm(1,DREAL(DELTA)*ZETA,R) ! Bessel function of 1st kind and order 1
c	J2 = Jm(2,DREAL(DELTA)*ZETA,R) ! Bessel function of 1st kind and order 2
C
	G1 = DELTA*ZETA*CURL1/2.0D0
	G2 = DELTA*ZETA*CURL2/2.0D0
	G3 = DELTA*ZETA/2.0D0
	G4 = DELTA*KSI1
	G5 = DELTA*KSI2
C
	H21 = C44*(ALPHA*(DELTA*KSI1)**2.0D0
     *		-(KAPPA-1)*(DELTA*ZETA)**2.0D0*CURL1)
	H22 = C44*(ALPHA*(DELTA*KSI2)**2.0D0
     *		-(KAPPA-1)*(DELTA*ZETA)**2.0D0*CURL2)
	H41 = C44*DELTA*ZETA/2.0D0*(1+CURL1)*DELTA*KSI1
	H42 = C44*DELTA*ZETA/2.0D0*(1+CURL2)*DELTA*KSI2
	H43 = C44*DELTA*ZETA/2.0D0*DELTA*KSI3
C
c	LAMBDA = (g1*g5-g4*g2)
Cc
c	Kn1(1,1)=1.0D0/2.0D0*(h41*g5*g3-h42*g4*g3+h43*g1*g5-h43*g4*g2)/g3
c	Kn1(1,2)=1.0D0/2.0D0*J2*(-h41*g5*g3+h42*g4*g3+h43*g1*g5
c     *												-h43*g4*g2)/J0/g3
c	Kn1(1,3)=2.0D0*J2*(-h41*g2+h42*g1)/J1
c	Kn1(2,1)=1.0D0/2.0D0*J0*(-h41*g5*g3+h42*g4*g3+h43*g1*g5
c     *												-h43*g4*g2)/J2/g3
c	Kn1(2,2)=1.0D0/2.0D0*(h41*g5*g3-h42*g4*g3+h43*g1*g5-h43*g4*g2)/g3
c	Kn1(2,3)=-2.0D0*J0*(-h41*g2+h42*g1)/J1
c	Kn1(3,1)=-1.0D0/4.0D0*J1*(-h21*g5+h22*g4)/J2
c	Kn1(3,2)=1.0D0/4.0D0*J1*(-h21*g5+h22*g4)/J0
c	Kn1(3,3)=(-h21*g2+h22*g1)
Cc
c	Kn1 = Kn1/LAMBDA
C
	LAMBDA = g1*g5-g2*g4
	Kn1(1,1)=1.0D0/2.0D0*(h41*g5*g3-h42*g4*g3+h43*g1*g5-h43*g2*g4)/g3
	Kn1(1,2)=1.0D0/2.0D0*(-h41*g5*g3+h42*g4*g3+h43*g1*g5-h43*g2*g4)/g3
	Kn1(1,3)=-2.0D0*h41*g2+2.0D0*h42*g1
	Kn1(2,1)=1.0D0/2.0D0*(-h41*g5*g3+h42*g4*g3+h43*g1*g5-h43*g2*g4)/g3
	Kn1(2,2)=1.0D0/2.0D0*(h41*g5*g3-h42*g4*g3+h43*g1*g5-h43*g2*g4)/g3
	Kn1(2,3)=2.0D0*h41*g2-2*h42*g1
	Kn1(3,1)=1.0D0/4.0D0*h21*g5-1.0D0/4.0D0*h22*g4
	Kn1(3,2)=-1.0D0/4.0D0*h21*g5+1.0D0/4.0D0*h22*g4
	Kn1(3,3)=-h21*g2+h22*g1
c
	Kn1 = Kn1/LAMBDA
C
	END SUBROUTINE DETERMINEkn1
