	SUBROUTINE INTEGRATE(CODE, CODEterm, A, B, RESULTADO)
C
	USE NUCLEOS
C	USE Numerical_Libraries
C
	IMPLICIT NONE
C	
	INTEGER, INTENT(IN) :: CODE, CODEterm
	REAL*8, INTENT(IN) :: A,B
	REAL*8, INTENT(OUT) :: RESULTADO
C
	REAL*8 :: EPSABS,EPSREL
	INTEGER, PARAMETER :: LIMIT=50000
	REAL*8 :: ABSERR
	REAL*8, DIMENSION(LIMIT) :: ALIST, BLIST, RLIST, ELIST
	INTEGER, DIMENSION(LIMIT) :: IORD
	INTEGER :: IER, NEVAL, LAST
C
	EPSABS = 1.0D-6
	EPSREL = 1.0D-6
C
	IF (CODE.EQ.0) THEN
C
		IF (CODEterm.EQ.11) THEN
			CALL DQAGE(RURFxint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.12) THEN
			CALL DQAGE(RURMyint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.21) THEN
			CALL DQAGE(RUZFxint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.22) THEN
			CALL DQAGE(RUZMyint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.31) THEN
			CALL DQAGE(RUZZint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		END IF
C
	ELSE	
C
		IF (CODEterm.EQ.11) THEN
			CALL DQAGE(IURFxint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.12) THEN
			CALL DQAGE(IURMyint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.21) THEN
			CALL DQAGE(IUZFxint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.22) THEN
			CALL DQAGE(IUZMyint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		ELSE IF (CODEterm.EQ.31) THEN
			CALL DQAGE(IUZZint, A, B, EPSABS, EPSREL,
     *				5, LIMIT, RESULTADO,
     *				ABSERR, NEVAL, IER, ALIST, BLIST, RLIST, ELIST, 
     *				IORD, LAST)
		END IF
C
	END IF
C	
	END SUBROUTINE INTEGRATE
