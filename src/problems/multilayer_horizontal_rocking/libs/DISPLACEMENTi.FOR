	SUBROUTINE FIND_BOUNDARY(CODEterm, BOUNDARY)
C
	USE NUCLEOS
C
	IMPLICIT NONE
C	
	INTEGER, INTENT(IN) :: CODEterm
	REAL*8 :: BOUNDARY
	REAL*8 :: A, B, C
C
	A=0.0D0
	B=10.0D0
	DO 11
		IF (((CODEterm.EQ.11).AND.(ISNAN(RURFxint(B)))).OR.
     *		((CODEterm.EQ.12).AND.(ISNAN(RURMyint(B)))).OR.
     *		((CODEterm.EQ.21).AND.(ISNAN(RUZFxint(B)))).OR.
     *		((CODEterm.EQ.22).AND.(ISNAN(RUZMyint(B)))).OR.
     *		((CODEterm.EQ.31).AND.(ISNAN(RUZZint(B))))) THEN
			GO TO 12
		END IF
		A=B
		B=B+1000.0D0
          WRITE(*,*) B
11	CONTINUE
C
12	CONTINUE ! At this point Uzz(B) is NAN and Uzz(A) is numeric.
C
	DO 13
		IF ((B-A).LE.1.0D-3) THEN
			BOUNDARY = A
			GO TO 14
		ELSE
			C = (A+B)/2.0D0
			IF (((CODEterm.EQ.11).AND.(ISNAN(RURFxint(C)))).OR.
     *			((CODEterm.EQ.12).AND.(ISNAN(RURMyint(C)))).OR.
     *			((CODEterm.EQ.21).AND.(ISNAN(RUZFxint(C)))).OR.
     *			((CODEterm.EQ.22).AND.(ISNAN(RUZMyint(C)))).OR.
     *			((CODEterm.EQ.31).AND.(ISNAN(RUZZint(C))))) THEN
				B=C
			ELSE
				A=C
			END IF
		END IF
13	CONTINUE
C
14	CONTINUE
C
	END SUBROUTINE FIND_BOUNDARY
