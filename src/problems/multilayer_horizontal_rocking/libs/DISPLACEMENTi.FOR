	SUBROUTINE DISPLACEMENTi(INTEGBOUNDARY, CODEterm, DISP)
C
	USE NUCLEOS
	IMPLICIT NONE
C
	REAL*8, INTENT(IN) :: INTEGBOUNDARY
	INTEGER, INTENT(IN) :: CODEterm
	COMPLEX*16, INTENT(OUT) :: DISP
	REAL*8 :: rOUT, iOUT
C
	CALL INTEGRATE(0, CODEterm, 0.0D0, INTEGBOUNDARY, rOUT)
	CALL INTEGRATE(1, CODEterm, 0.0D0, INTEGBOUNDARY, iOUT)
	DISP = DCMPLX(rOUT, iOUT)
C
	END SUBROUTINE DISPLACEMENTi
