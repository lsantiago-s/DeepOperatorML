C
	PROGRAM MULTILAYER
C
	USE NUCLEOS
C
	IMPLICIT NONE
C
	REAL*8, DIMENSION(100,8) :: PROPERTIES
	REAL*8 :: R, OMEGA, hZ
	REAL*8 :: S1, S2, B, L
	REAL*8 :: C11i, C12i, C13i, C33i, C44i, ETAi, RHOi, hZi
	REAL*8 :: INTEGBOUNDARYRFx ,INTEGBOUNDARYRMy
	REAL*8 :: INTEGBOUNDARYTFx, INTEGBOUNDARYZMy, INTEGBOUNDARYZZ
	INTEGER :: I, J, M
	INTEGER :: N, Nload, Nrec
	INTEGER :: KOMEGA
	COMPLEX*16 :: URFxout, URMyout, UTFxout, UZMyout, UZZout
	CHARACTER*40 :: INPUTFILENAME, OUTPUTFILENAME
	REAL*8 :: OMEGAi, OMEGAf, OMEGAinc
	INTEGER :: CODExx, CODExf, CODEfx, CODEff
C     
	COMMON /PROPS/ PROPERTIES
	COMMON /PHYSICS/ R, OMEGA
	COMMON /SIZE/ N, Nload, Nrec
	COMMON /RADIUS/ S1, S2
C
C	READING INPUT DATA
C
	INPUTFILENAME = 'INPUT.TXT'
	OPEN(UNIT=11, FILE=INPUTFILENAME,STATUS='UNKNOWN')   
	READ(11,*) OMEGAi, OMEGAf, OMEGAinc
	READ(11,*) N, Nload, B, M, OUTPUTFILENAME
	READ(11,*) CODExx, CODExf, CODEfx, CODEff

	DO 20 I=1,N+1
		READ(11,*) C11i, C12i, C13i, C33i, C44i, ETAi, RHOi, hZi
		PROPERTIES(I, 1) = C11i
		PROPERTIES(I, 2) = C12i
		PROPERTIES(I, 3) = C13i
		PROPERTIES(I, 4) = C33i
		PROPERTIES(I, 5) = C44i
		PROPERTIES(I, 6) = ETAi
		PROPERTIES(I, 7) = RHOi
		PROPERTIES(I, 8) = hZi
20	CONTINUE
	CLOSE(UNIT=11)
C
C	CALCULATING
C
	L = (1.0D0-B)/M

	OPEN(UNIT=10, FILE='SAIDA_URFx_W.out',STATUS='UNKNOWN')
	OPEN(UNIT=11, FILE='SAIDA_UZFx_W.out',STATUS='UNKNOWN')
	OPEN(UNIT=12, FILE='SAIDA_URMy_W.out',STATUS='UNKNOWN')
	OPEN(UNIT=13, FILE='SAIDA_UZMy_W.out',STATUS='UNKNOWN')
	OPEN(UNIT=14, FILE='SAIDA_UZZ_W.out',STATUS='UNKNOWN')
C
	DO 21 KOMEGA = 0, INT((OMEGAf-OMEGAi)/OMEGAinc + 0.5D0)
		OMEGA = OMEGAi + OMEGAinc*KOMEGA
C
		WRITE(*,*)'OMEGA **************',OMEGA

		WRITE(10,*) 'OMEGA=',OMEGA
		WRITE(11,*) 'OMEGA=',OMEGA
		WRITE(12,*) 'OMEGA=',OMEGA
		WRITE(13,*) 'OMEGA=',OMEGA
		WRITE(14,*) 'OMEGA=',OMEGA

C		Loop over RECEIVER interface
		DO 19 Nrec = 1, N+1
			WRITE(*,*) 'RECEIVER INTERFACE Nrec=', Nrec
			WRITE(10,*) 'Nrec=', Nrec
			WRITE(11,*) 'Nrec=', Nrec
			WRITE(12,*) 'Nrec=', Nrec
			WRITE(13,*) 'Nrec=', Nrec
			WRITE(14,*) 'Nrec=', Nrec

			DO 22 I=1,M
				WRITE(*,*) 'PROGRESS',I
				R = B + L*(I-1.0D0) + L/2.0D0

				DO 23 J=1,M
					WRITE(*,*) 'PROGRESS','    ',j

					S1 = B + L*(J-1.0D0)
					S2 = B + L*J

					INTEGBOUNDARYRFx = 230.0D0/OMEGA-5.0D0
					INTEGBOUNDARYRMy = 230.0D0/OMEGA-5.0D0
					INTEGBOUNDARYTFx = 230.0D0/OMEGA-5.0D0
					INTEGBOUNDARYZMy = 230.0D0/OMEGA-5.0D0
					INTEGBOUNDARYZZ = 230.0D0/OMEGA-5.0D0

					IF (CODExx.EQ.1) THEN
						CALL DISPLACEMENTi(INTEGBOUNDARYRFx, 11, URFxout)
					END IF
					IF (CODExf.EQ.1) THEN  
						CALL DISPLACEMENTi(INTEGBOUNDARYRMy, 12, URMyout)
					END IF
					IF (CODEfx.EQ.1) THEN
						CALL DISPLACEMENTi(INTEGBOUNDARYTFx, 21, UTFxout)
					END IF
					IF (CODEff.EQ.1) THEN  
						CALL DISPLACEMENTi(INTEGBOUNDARYZMy, 22, UZMyout) 
					END IF
					CALL DISPLACEMENTi(INTEGBOUNDARYZZ, 31, UZZout)

					WRITE(10,*) DREAL(URFxout), DIMAG(URFxout) 
					WRITE(11,*) DREAL(UTFxout), DIMAG(UTFxout)
					WRITE(12,*) DREAL(URMyout), DIMAG(URMyout)
 					WRITE(13,*) DREAL(UZMyout), DIMAG(UZMyout)
					WRITE(14,*) DREAL(UZZout), DIMAG(UZZout)
23				CONTINUE
22			CONTINUE
19		CONTINUE
21	CONTINUE
C
	CLOSE(UNIT=10)
	CLOSE(UNIT=11)
	CLOSE(UNIT=12)
	CLOSE(UNIT=13)
	CLOSE(UNIT=14)
C
	END PROGRAM MULTILAYER
C
