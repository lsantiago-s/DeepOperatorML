C
	SUBROUTINE UZFx(ZETA, CODE, VALUE)
C
	USE NUCLEOS
C
	IMPLICIT NONE
C
	REAL*8, INTENT(IN) :: ZETA
	REAL*8, INTENT(OUT) :: VALUE
	INTEGER, INTENT(IN) :: CODE
	REAL*8 :: PI
C
	REAL*8, DIMENSION(100,8) :: PROPERTIES
	INTEGER :: N, Nload, Nrec
	INTEGER :: I, J
	REAL*8 :: R, OMEGA, RHO, S1, S2, ETA
	COMPLEX*16 :: DELTA, C44
C
	INTEGER :: nCOUNTER
	COMPLEX*16, DIMENSION(3*(N+1),3*(N+1)) :: Kg
	COMPLEX*16, DIMENSION(3*(N+1)) :: P, U
	COMPLEX*16, DIMENSION(6,6) :: Kn
	COMPLEX*16, DIMENSION(3,3) :: Kn1
C
	COMMON /SIZE/ N, Nload, Nrec
	COMMON /PROPS/ PROPERTIES
	COMMON /PHYSICS/ R, OMEGA
	COMMON /RADIUS/ S1, S2
C
          ETA = PROPERTIES(1,6)
	    RHO = PROPERTIES(1,7)
	    C44 = PROPERTIES(1,5)*DCMPLX(1.0D0, ETA)
	    DELTA = CDSQRT(RHO*(OMEGA*CDSQRT(c44/RHO))**2.0D0/C44)
C
	PI = 3.141592653589793238462643383279D0
C
	KG = DCMPLX(0.0D0, 0.0D0)
C
	DO 13 nCOUNTER=1,N
		CALL DETERMINEkn(nCOUNTER, Kn, ZETA)
		DO 11 I=1,6
			DO 12 J=1,6
				Kg(I+3*(nCOUNTER-1), J+3*(nCOUNTER-1)) = 
     *			Kg(I+3*(nCOUNTER-1), J+3*(nCOUNTER-1)) + Kn(I,J)
12			CONTINUE
11		CONTINUE
13	CONTINUE
C
	CALL DETERMINEkn1(Kn1, ZETA)
C
	DO 14 I=1,3
		DO 15 J=1,3
			Kg(I+3*N, J+3*N) = Kg(I+3*N, J+3*N) + Kn1(I,J)
15		CONTINUE
14	CONTINUE
C
C	Load applied at Nload (unchanged)
	P = DCMPLX(0.0D0, 0.0D0)
	P(3*Nload-2) = 0.0D0
	P(3*Nload-1) = 2.0D0*H0(S1, S2, DREAL(DELTA)*ZETA)
C
	U = DCMPLX(0.0D0, 0.0D0)
	CALL ZSIST(Kg, P, 3*(N+1), 3*(N+1), U)
C
C	Readout at Nrec (changed)
	IF (CODE.EQ.0) THEN
		VALUE = DREAL(DELTA**2.0D0*U(3*Nrec))
		VALUE = -VALUE*ZETA*Jm(1, DREAL(DELTA)*ZETA, R)
	ELSE
		VALUE = DIMAG(DELTA**2.0D0*U(3*Nrec))
		VALUE = -VALUE*ZETA*Jm(1, DREAL(DELTA)*ZETA, R)
	END IF
C
	END SUBROUTINE UZFx
