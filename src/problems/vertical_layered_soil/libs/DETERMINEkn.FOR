C
	SUBROUTINE DETERMINEkn(n, Kn, ZETA)
C
	USE Nucleos
C	USE Numerical_Libraries
C
	IMPLICIT NONE
C
	COMPLEX*16, DIMENSION(6,6), INTENT(OUT) :: Kn
	INTEGER, INTENT(IN) :: n
	REAL*8, INTENT(IN) :: ZETA
C
	REAL*8, DIMENSION(100,8) :: PROPERTIES
	REAL*8 :: R, OMEGA
C
      real*8, dimension(72) :: rwork
C
	REAL*8 :: ETA, RHO, ZN, ZN1
	COMPLEX*16 :: C11, C12, C13, C33, C44
	COMPLEX*16 :: ALPHA, BETAb, GAMMAb, SIGMA, KAPPA, DELTA
	COMPLEX*16 :: CURL1, CURL2, KSI1, KSI2, KSI3, PHI
c	COMPLEX*16 :: A1, A2, A3, A4, A5, A6, A7, A8
c	COMPLEX*16 :: B51, B52, B53, B41, B42, B43, B21, B22
	COMPLEX*16 :: G1, G2, G3, G4, G5
	COMPLEX*16 :: H41, H42, H43, H21, H22
	COMPLEX*16, DIMENSION(6,6) :: Gn, Fn, GnINV
	COMPLEX*16 :: LAMBDA
C     
      INTEGER :: I,J,K
C
	COMMON /PROPS/ PROPERTIES
	COMMON /PHYSICS/ R, OMEGA
C
C	PROPERTIES = [C11 C12 C13 C33 C44 eta RHO Z(n+1)=hZ]  
	ETA = PROPERTIES(n,6)
	C11 = PROPERTIES(n,1)*DCMPLX(1.0D0, ETA)
	C12 = PROPERTIES(n,2)*DCMPLX(1.0D0, ETA)
	C13 = PROPERTIES(n,3)*DCMPLX(1.0D0, ETA)
	C33 = PROPERTIES(n,4)*DCMPLX(1.0D0, ETA)
	C44 = PROPERTIES(n,5)*DCMPLX(1.0D0, ETA)
	RHO = PROPERTIES(n,7)
	zN  = 0.0D0
	zN1 = PROPERTIES(n,8)
C
	ALPHA = C33/C44*DCMPLX(1.0D0, ETA)
	BETAb = C11/C44*DCMPLX(1.0D0, ETA)
	KAPPA = (C13 + C44)/C44*DCMPLX(1.0D0, ETA)
	GAMMAb = (1.0D0 + ALPHA*BETAb - KAPPA**2.0D0)*DCMPLX(1.0D0, ETA)
	SIGMA = 0.5D0*(C11-C12)/C44*DCMPLX(1.0D0, ETA)
	DELTA = CDSQRT(RHO*OMEGA**2.0D0/C44)*DCMPLX(1.0D0, ETA)
C
	PHI = (GAMMAb*ZETA**2.0D0-1.0D0-ALPHA)**2.0D0
     *-4.0D0*ALPHA*(BETAb*ZETA**4.0D0-(BETAb+1.0D0)*ZETA**2.0D0+1.0D0)
C
	KSI1 = CDSQRT((GAMMAb*ZETA**2.0D0-1.0D0-ALPHA+CDSQRT(PHI))
     *			/(2.0D0*ALPHA))
	KSI2 = CDSQRT((GAMMAb*ZETA**2.0D0-1.0D0-ALPHA-CDSQRT(PHI))
     *			/(2.0D0*ALPHA))
	KSI3 = CDSQRT(SIGMA*ZETA**2.0D0-1.0D0)
C
	CURL1 = (ALPHA*KSI1**2.0D0-ZETA**2.0D0+1.0D0)/(KAPPA*ZETA**2.0D0)
	CURL2 = (ALPHA*KSI2**2.0D0-ZETA**2.0D0+1.0D0)/(KAPPA*ZETA**2.0D0)
C
	G1 = DELTA*ZETA*CURL1/2.0D0
	G2 = DELTA*ZETA*CURL2/2.0D0
	G3 = DELTA*ZETA/2.0D0
	G4 = DELTA*KSI1
	G5 = DELTA*KSI2
C
	H21 = C44*(ALPHA*(DELTA*KSI1)**2.0D0
     *		-(KAPPA-1)*(DELTA*ZETA)**2.0D0*CURL1)
	H22 = C44*(ALPHA*(DELTA*KSI2)**2.0D0
     *		-(KAPPA-1)*(DELTA*ZETA)**2.0D0*CURL2)
	H41 = C44*DELTA*ZETA/2.0D0*(1+CURL1)*DELTA*KSI1
	H42 = C44*DELTA*ZETA/2.0D0*(1+CURL2)*DELTA*KSI2
	H43 = C44*DELTA*ZETA/2.0D0*DELTA*KSI3
C
	LAMBDA = 8.0D0*g1*g4*g2*g5+g1**2.0D0*CDEXP(-DELTA*(zN-zN1)
     **(KSI1+KSI2))*g5**2.0D0-g1**2.0D0*CDEXP(-DELTA*(zN-zN1)*
     *(KSI1-KSI2))*g5**2.0D0-2*g1*CDEXP(-DELTA*(zN-zN1)*(KSI1+
     *KSI2))*g4*g5*g2-2*g1*CDEXP(-DELTA*(zN-zN1)*(KSI1-KSI2))*
     *g4*g2*g5+g4**2.0D0*CDEXP(-DELTA*(zN-zN1)*(KSI1+KSI2))*g2**2.0D0
     *-g4**2.0D0*CDEXP(-DELTA*(zN-zN1)*(KSI1-KSI2))*g2**2.0D0-g1**2.0D0
     **CDEXP(DELTA*(zN-zN1)*(KSI1-KSI2))*g5**2.0D0+g1**2.0D0*
     *CDEXP(DELTA*(zN-zN1)*(KSI1+KSI2))*g5**2.0D0-2*g1*CDEXP(DELTA*
     *(zN-zN1)*(KSI1-KSI2))*g4*g5*g2-2*g1*CDEXP(DELTA*(zN-zN1)*(KSI1+
     *KSI2))*g4*g2*g5-g4**2.0D0*CDEXP(DELTA*(zN-zN1)*(KSI1-KSI2))
     **g2**2.0D0+g4**2.0D0*CDEXP(DELTA*(zN-zN1)*(KSI1+KSI2))*g2**2.0D0
C
	GnINV(1,1)=-0.25D0*(2*g4*CDEXP(DELTA*KSI1*zN)*g2+g1*CDEXP(DELTA
     **(KSI1*zN1-zN*KSI2+KSI2*zN1))*g5-g1*CDEXP(DELTA*(KSI1*zN1-KSI2*
     *zN1+zN*KSI2))*g5-g4*CDEXP(DELTA*(KSI1*zN1-zN*KSI2+KSI2*zN1))*g2
     *-g4*CDEXP(DELTA*(KSI1*zN1-KSI2*zN1+zN*KSI2))*g2)*g5
	GnINV(1,2)=0.25D0*(2*g4*CDEXP(DELTA*KSI1*zN)*g2+g1*CDEXP(DELTA*
     *(KSI1*zN1-zN*KSI2+KSI2*zN1))*g5-g1*CDEXP(DELTA*(KSI1*zN1-KSI2*
     *zN1+zN*KSI2))*g5-g4*CDEXP(DELTA*(KSI1*zN1-zN*KSI2+KSI2*zN1))*g2
     *-g4*CDEXP(DELTA*(KSI1*zN1-KSI2*zN1+zN*KSI2))*g2)*g5
	GnINV(1,3)=-(2*g1*CDEXP(DELTA*KSI1*zN)*g5-g1*CDEXP(DELTA*(KSI1*
     *zN1-zN*KSI2+KSI2*zN1))*g5-g1*CDEXP(DELTA*(KSI1*zN1-KSI2*zN1+zN*
     *KSI2))*g5+g4*CDEXP(DELTA*(KSI1*zN1-zN*KSI2+KSI2*zN1))*g2-g4*
     *CDEXP(DELTA*(KSI1*zN1-KSI2*zN1+zN*KSI2))*g2)*g2
	GnINV(1,4)=-0.25D0*g5*(-g1*CDEXP(DELTA*(KSI1*zN-zN*KSI2+KSI2*zN1))
     **g5+g1*CDEXP(DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2))*g5-g4*CDEXP(DELTA
     **(KSI1*zN-zN*KSI2+KSI2*zN1))*g2-g4*CDEXP(DELTA*(KSI1*zN-KSI2*zN1
     *+zN*KSI2))*g2+2*g4*CDEXP(DELTA*KSI1*zN1)*g2)
	GnINV(1,5)=0.25D0*g5*(-g1*CDEXP(DELTA*(KSI1*zN-zN*KSI2+KSI2*zN1))
     **g5+g1*CDEXP(DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2))*g5-g4*CDEXP(DELTA
     **(KSI1*zN-zN*KSI2+KSI2*zN1))*g2-g4*CDEXP(DELTA*(KSI1*zN-KSI2*zN1
     *+zN*KSI2))*g2+2*g4*CDEXP(DELTA*KSI1*zN1)*g2)
	GnINV(1,6)=-g2*(-g1*CDEXP(DELTA*(KSI1*zN-zN*KSI2+KSI2*zN1))*g5-g1
     **CDEXP(DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2))*g5-g4*CDEXP(DELTA*(KSI1
     **zN-zN*KSI2+KSI2*zN1))*g2+g4*CDEXP(DELTA*(KSI1*zN-KSI2*zN1+zN
     **KSI2))*g2+2*g1*CDEXP(DELTA*KSI1*zN1)*g5)
C
	GnINV(2,1)=0.25D0*(-2*g4*CDEXP(-DELTA*KSI1*zN)*g2+g1*CDEXP(-DELTA
     **(KSI1*zN1-KSI2*zN1+zN*KSI2))*g5-g1*CDEXP(-DELTA*(KSI1*zN1-zN*KSI2
     *+KSI2*zN1))*g5+g4*CDEXP(-DELTA*(KSI1*zN1-KSI2*zN1+zN*KSI2))*g2+
     *g4*CDEXP(-DELTA*(KSI1*zN1-zN*KSI2+KSI2*zN1))*g2)*g5
	GnINV(2,2)=-0.25D0*(-2*g4*CDEXP(-DELTA*KSI1*zN)*g2+g1*CDEXP(-
     *DELTA*(KSI1*zN1-KSI2*zN1+zN*KSI2))*g5-g1*CDEXP(-DELTA*(KSI1*zN1
     *-zN*KSI2+KSI2*zN1))*g5+g4*CDEXP(-DELTA*(KSI1*zN1-KSI2*zN1+zN*KSI2
     *))*g2+g4*CDEXP(-DELTA*(KSI1*zN1-zN*KSI2+KSI2*zN1))*g2)*g5
	GnINV(2,3)=-(-2*g1*CDEXP(-DELTA*KSI1*zN)*g5+g1*CDEXP(-DELTA*(KSI1
     **zN1-KSI2*zN1+zN*KSI2))*g5+g1*CDEXP(-DELTA*(KSI1*zN1-zN*KSI2+KSI2
     **zN1))*g5+g4*CDEXP(-DELTA*(KSI1*zN1-KSI2*zN1+zN*KSI2))*g2-g4*
     *CDEXP(-DELTA*(KSI1*zN1-zN*KSI2+KSI2*zN1))*g2)*g2
	GnINV(2,4)=0.25D0*g5*(-g1*CDEXP(-DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2))
     **g5+g1*CDEXP(-DELTA*(KSI1*zN-zN*KSI2+KSI2*zN1))*g5+g4*CDEXP(-
     *DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2))*g2+g4*CDEXP(-DELTA*(KSI1*zN-zN
     **KSI2+KSI2*zN1))*g2-2*g4*CDEXP(-DELTA*KSI1*zN1)*g2)
	GnINV(2,5)=-0.25D0*g5*(-g1*CDEXP(-DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2
     *))*g5+g1*CDEXP(-DELTA*(KSI1*zN-zN*KSI2+KSI2*zN1))*g5+g4*CDEXP(-
     *DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2))*g2+g4*CDEXP(-DELTA*(KSI1*zN-zN
     **KSI2+KSI2*zN1))*g2-2*g4*CDEXP(-DELTA*KSI1*zN1)*g2)
	GnINV(2,6)=-g2*(g1*CDEXP(-DELTA*(KSI1*zN-KSI2*zN1+zN*KSI2))*g5+
     *g1*CDEXP(-DELTA*(KSI1*zN-zN*KSI2+KSI2*zN1))*g5-g4*CDEXP(-DELTA*
     *(KSI1*zN-KSI2*zN1+zN*KSI2))*g2+g4*CDEXP(-DELTA*(KSI1*zN-zN*KSI2
     *+KSI2*zN1))*g2-2*g1*CDEXP(-DELTA*KSI1*zN1)*g5)
C
	GnINV(3,1)=-0.25D0*g4*(-CDEXP(-DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1
     *))*g1*g5+g4*CDEXP(-DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g2-g1*
     *CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g5+2*g1*CDEXP(DELTA*KSI2
     **zN)*g5-g4*CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g2)
	GnINV(3,2)=0.25D0*g4*(-CDEXP(-DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))
     **g1*g5+g4*CDEXP(-DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g2-g1*
     *CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g5+2*g1*CDEXP(DELTA*KSI2
     **zN)*g5-g4*CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g2)
	GnINV(3,3)=-g1*(CDEXP(-DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g1*g5-
     *g4*CDEXP(-DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g2-g1*CDEXP(DELTA*
     *(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g5+2*CDEXP(DELTA*KSI2*zN)*g4*g2-
     *g4*CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g2)
	GnINV(3,4)=0.25D0*(-2*g1*CDEXP(DELTA*KSI2*zN1)*g5+g1*CDEXP(-
     *DELTA*(KSI1*zN-KSI1*zN1-zN*KSI2))*g5+g4*CDEXP(-DELTA*(KSI1*zN-
     *KSI1*zN1-zN*KSI2))*g2+g1*CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2
     *))*g5-CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2))*g4*g2)*g4
	GnINV(3,5)=-0.25D0*(-2*g1*CDEXP(DELTA*KSI2*zN1)*g5+g1*CDEXP(-
     *DELTA*(KSI1*zN-KSI1*zN1-zN*KSI2))*g5+g4*CDEXP(-DELTA*(KSI1*zN-
     *KSI1*zN1-zN*KSI2))*g2+g1*CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2
     *))*g5-CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2))*g4*g2)*g4
	GnINV(3,6)=g1*(-2*CDEXP(DELTA*KSI2*zN1)*g4*g2+g1*CDEXP(-DELTA*
     *(KSI1*zN-KSI1*zN1-zN*KSI2))*g5+g4*CDEXP(-DELTA*(KSI1*zN-KSI1*
     *zN1-zN*KSI2))*g2-g1*CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2))*
     *g5+CDEXP(DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2))*g4*g2)
C
	GnINV(4,1)=0.25D0*(CDEXP(-DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))
     **g1*g5+g4*CDEXP(-DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g2+g1*
     *CDEXP(DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g5-2*g1*CDEXP(-DELTA
     **KSI2*zN)*g5-g4*CDEXP(DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g2)*g4
	GnINV(4,2)=-0.25D0*(CDEXP(-DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))
     **g1*g5+g4*CDEXP(-DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g2+g1*
     *CDEXP(DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g5-2*g1*CDEXP(-DELTA
     **KSI2*zN)*g5-g4*CDEXP(DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g2)*g4
	GnINV(4,3)=-g1*(CDEXP(-DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g1
     **g5+g4*CDEXP(-DELTA*(-KSI1*zN1+KSI1*zN+KSI2*zN1))*g2-g1*
     *CDEXP(DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g5-2*CDEXP(-DELTA*KSI2
     **zN)*g4*g2+g4*CDEXP(DELTA*(KSI1*zN-KSI1*zN1-KSI2*zN1))*g2)
	GnINV(4,4)=0.25D0*(-2*g1*CDEXP(-DELTA*KSI2*zN1)*g5+g1*
     *CDEXP(-DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2))*g5-g4*CDEXP(-DELTA*
     *(-KSI1*zN1+KSI1*zN+zN*KSI2))*g2+CDEXP(DELTA*(KSI1*zN-KSI1*zN1
     *-zN*KSI2))*g1*g5+g4*CDEXP(DELTA*(KSI1*zN-KSI1*zN1-zN*KSI2))*g2)*g4
	GnINV(4,5)=-0.25D0*(-2*g1*CDEXP(-DELTA*KSI2*zN1)*g5+g1*CDEXP(
     *-DELTA*(-KSI1*zN1+KSI1*zN+zN*KSI2))*g5-g4*CDEXP(-DELTA*(-KSI1*
     *zN1+KSI1*zN+zN*KSI2))*g2+CDEXP(DELTA*(KSI1*zN-KSI1*zN1-zN*KSI2
