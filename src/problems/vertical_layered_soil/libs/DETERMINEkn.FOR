	GnINV(4,6)=-(-2*CDEXP(-DELTA*KSI2*zN1)*g4*g2-g1*CDEXP(-DELTA*(
     *-KSI1*zN1+KSI1*zN+zN*KSI2))*g5+g4*CDEXP(-DELTA*(-KSI1*zN1+KSI1
     **zN+zN*KSI2))*g2+CDEXP(DELTA*(KSI1*zN-KSI1*zN1-zN*KSI2))*g1*g5
     *+g4*CDEXP(DELTA*(KSI1*zN-KSI1*zN1-zN*KSI2))*g2)*g1
C
