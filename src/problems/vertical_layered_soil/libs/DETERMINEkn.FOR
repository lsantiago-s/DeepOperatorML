     *g4**2.0D0*CDEXP(DELTA*(zN-zN1)*(KSI1+KSI2))*g2**2.0D0)/g3/
     *(-CDEXP(DELTA*KSI3*(zN-zN1))+CDEXP(-DELTA*KSI3*(zN-zN1)))
